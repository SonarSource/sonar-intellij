package org.sonarlint.intellij.ui.scope;

import com.intellij.openapi.project.Project;
import com.intellij.openapi.vcs.changes.ChangeListManager;
import com.intellij.openapi.vfs.VirtualFile;

import java.util.Collection;

public class ModifiedFilesScope extends IssueTreeScope {
  private final Project project;

  public ModifiedFilesScope(Project project) {
    this.project = project;
  }

  @Override
  public String getDisplayName() {
    return "Modified files";
  }

  @Override
  public Collection<VirtualFile> getAll() {
    return ChangeListManager.getInstance(project).getAffectedFiles();
  }
}
